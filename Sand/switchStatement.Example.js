// switch statement 

var season = 'fuck'; 

switch(season){
    case 'summer':
        console.log("It's summer!");
        break;
    case 'fall':
        console.log("It's fall!");
        break;
    case 'winter':
        console.log("It's winter!");
        break;
    case 'spring':
        console.log("It's spring!");
        break;
    default:
        console.log("There's only 4 seasons!");
        break;
}